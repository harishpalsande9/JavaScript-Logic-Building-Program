console.log("Ternary operator for multiple conditions Code Example");

let number = 3;

if (number == 1) {
  console.log("One");
} else if (number == 2) {
  console.log("Two");
} else if (number == 3) {
  console.log("Three");
} else {
  console.log("Non Of Them");
}

// Or Using Ternary Operator

number == 1
  ? console.log("One")
  : number == 2
  ? console.log("Two")
  : number == 3
  ? console.log("Three")
  : console.log("Non Of Them");
